fun main() {
    val x = 45
    val y = 39

    // greater than
    println("x > y = " +  (x > y))

    // less than
    println("x < y = " +  (x < y))

    // greater than or equal to
    println("x >= y = " +  (x >= y))

    // less than or equal to
    println("x <= y = " +  (x <= y))

    // equal to
    println("x == y = " +  (x == y))

    // not equal to
    println("x != y = " +  (x != y))

}